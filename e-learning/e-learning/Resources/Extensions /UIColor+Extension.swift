import Foundation
import UIKit

extension UIColor {
    @nonobjc class var pinkColor: UIColor {
        return UIColor(red: 255/255, green: 102/255, blue: 128/255, alpha: 1.0)
    }
    
    @nonobjc class var purpleColor: UIColor {
        return UIColor(red: 101/255, green: 72/255, blue: 163/255, alpha: 1.0)
    }
}
